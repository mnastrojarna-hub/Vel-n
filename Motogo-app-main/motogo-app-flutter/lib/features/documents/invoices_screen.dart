/// Invoices list — mirrors s-invoices from templates-done-pages.js.
class InvoicesScreen extends ConsumerWidget {
  const InvoicesScreen({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final invoicesAsync = ref.watch(invoicesProvider);

    return Scaffold(
      backgroundColor: MotoGoColors.bg,
      appBar: AppBar(
        leading: GestureDetector(
          onTap: () => context.pop(),
          child: Center(
            child: Container(
              width: 36, height: 36,
              decoration: BoxDecoration(color: MotoGoColors.green, borderRadius: BorderRadius.circular(10)),
              child: const Center(child: Text('←', style: TextStyle(fontSize: 18, fontWeight: FontWeight.w900, color: Colors.black))),
            ),
          ),
        ),
        title: Row(children: [ClipRRect(borderRadius: BorderRadius.circular(8), child: Image.asset('assets/logo.png', width: 24, height: 24, fit: BoxFit.cover, errorBuilder: (_, __, ___) => Container(width: 24, height: 24, decoration: BoxDecoration(color: MotoGoColors.green, borderRadius: BorderRadius.circular(8)), child: const Icon(Icons.motorcycle, size: 14, color: Colors.black)))), const SizedBox(width: 8), Text(t(context).tr('invoicesTitle'))]), backgroundColor: MotoGoColors.dark),
      body: invoicesAsync.when(
        data: (invoices) {
          if (invoices.isEmpty) {
            return Center(child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                const Text('🧾', style: TextStyle(fontSize: 48)),
                const SizedBox(height: 12),
                Text(t(context).tr('noInvoices'), style: const TextStyle(fontSize: 14, fontWeight: FontWeight.w700, color: MotoGoColors.black)),
                Text(t(context).tr('invoicesAutoGenerated'), style: const TextStyle(fontSize: 12, color: MotoGoColors.g400)),
              ],
            ));
          }

          final grouped = <int, List<UserInvoice>>{};
          for (final inv in invoices) {
            grouped.putIfAbsent(inv.createdAt.year, () => []).add(inv);
          }
          final years = grouped.keys.toList()..sort((a, b) => b.compareTo(a));

          return ListView(
            padding: const EdgeInsets.all(16),
            children: [
              for (final year in years) ...[
                Padding(
                  padding: const EdgeInsets.only(bottom: 8, top: 8),
                  child: Text('$year', style: const TextStyle(fontSize: 14, fontWeight: FontWeight.w800, color: MotoGoColors.g400)),
                ),
                ...grouped[year]!.map((inv) => _InvoiceTile(invoice: inv)),
              ],
            ],
          );
        },
        loading: () => const Center(child: CircularProgressIndicator(color: MotoGoColors.green)),
        error: (e, st) {
          debugPrint('[INVOICES] Provider error: $e\n$st');
          return Center(child: Text('Chyba: $e', style: const TextStyle(color: MotoGoColors.red, fontSize: 12)));
        },
      ),
    );
  }
}

class _InvoiceTile extends StatelessWidget {
  final UserInvoice invoice;
  const _InvoiceTile({required this.invoice});

  @override
  Widget build(BuildContext context) {
    final isCN = invoice.isCreditNote;
    final date = '${invoice.createdAt.day}. ${invoice.createdAt.month}. ${invoice.createdAt.year}';

    return GestureDetector(
      onTap: () => _openInvoice(context),
      child: Container(
        padding: const EdgeInsets.all(12),
        margin: const EdgeInsets.only(bottom: 6),
        decoration: BoxDecoration(
          color: Colors.white,
          borderRadius: BorderRadius.circular(MotoGoTheme.radiusSm),
          border: isCN ? Border.all(color: MotoGoColors.red.withValues(alpha: 0.3)) : null,
        ),
        child: Row(children: [
          Text(_typeIcon(invoice.type), style: const TextStyle(fontSize: 20)),
          const SizedBox(width: 10),
          Expanded(child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(invoice.number ?? invoice.typeLabel,
                  style: TextStyle(fontSize: 13, fontWeight: FontWeight.w700, color: isCN ? MotoGoColors.red : MotoGoColors.black)),
              Text(date, style: const TextStyle(fontSize: 10, color: MotoGoColors.g400)),
            ],
          )),
          if (invoice.total != null)
            Text(
              '${isCN ? "−" : ""}${invoice.total!.toStringAsFixed(0)} Kč',
              style: TextStyle(fontSize: 13, fontWeight: FontWeight.w800, color: isCN ? MotoGoColors.red : MotoGoColors.black),
            ),
        ]),
      ),
    );
  }

  String _typeIcon(String type) => switch (type) {
    'proforma' || 'advance' => '🧾',
    'final' => '💰',
    'payment_receipt' => '✅',
    'shop_final' => '🛒',
    'credit_note' => '📕',
    _ => '📄',
  };

  Future<void> _openInvoice(BuildContext context) async {
    debugPrint('[INVOICES] Opening: id=${invoice.id}, type=${invoice.type}, number=${invoice.number}');

    // Fetch customer profile for invoice header
    String? customerName;
    String? customerAddress;
    try {
      final user = MotoGoSupabase.currentUser;
      if (user != null) {
        final profile = await MotoGoSupabase.client
            .from('profiles')
            .select('full_name, street, city, zip, country')
            .eq('id', user.id)
            .maybeSingle();
        if (profile != null) {
          customerName = profile['full_name'] as String?;
          final parts = [profile['street'], profile['city'], profile['zip'], profile['country']]
              .where((s) => s != null && (s as String).isNotEmpty)
              .join(', ');
          if (parts.isNotEmpty) customerAddress = parts;
        }
      }
    } catch (e) {
      debugPrint('[INVOICES] Profile fetch error: $e');
    }

    // Build HTML from invoice data
    final html = InvoiceHtmlBuilder.build(
      invoice,
      customerName: customerName,
      customerAddress: customerAddress,
    );

    if (!context.mounted) return;
    Navigator.of(context).push(MaterialPageRoute(
      builder: (_) => DocWebViewScreen(
        htmlContent: html,
        title: invoice.number ?? invoice.typeLabel,
      ),
    ));
  }
}
