<?php
// ===== MotoGo24 — Merge pages_<lang>_a/b/c.php → pages_<lang>.php =====
// Sloučí dílčí překlady (parts a/b/c) do finálního souboru per jazyk.
// Toleruje chybějící parts a merguje deep s existujícím `pages_<lang>.php`,
// takže ručně udržované překlady (typicky de/nl/pl) zůstanou zachované
// a parts jen doplní nové klíče / přepíší existující.
//
// Spusti: php motogo-web-php/scripts/merge_pages.php

$langs = ['en', 'de', 'es', 'fr', 'nl', 'pl'];
$langDir = __DIR__ . '/../lang/';

$loadFile = function ($path) {
    if (!is_file($path)) return null;
    $data = require $path;
    if (!is_array($data)) return null;
    if (isset($data['pages']) && is_array($data['pages'])) return $data['pages'];
    return $data;
};

foreach ($langs as $lang) {
    // 1) Začni stávajícím pages_<lang>.php (pokud existuje) — zachová ručně psané překlady.
    $existing = $loadFile($langDir . 'pages_' . $lang . '.php') ?? [];
    $mergedPages = $existing;
    $foundParts = 0;

    // 2) Aplikuj parts a/b/c (pokud existují) jako overlay přes deep merge.
    foreach (['a', 'b', 'c'] as $part) {
        $f = $langDir . 'pages_' . $lang . '_' . $part . '.php';
        $partPages = $loadFile($f);
        if ($partPages === null) continue;
        $mergedPages = _deepMerge($mergedPages, $partPages);
        $foundParts++;
    }

    if (empty($mergedPages)) {
        fwrite(STDERR, "[SKIP] {$lang}: nothing to write\n");
        continue;
    }

    $out = "<?php\n// MotoGo24 — pages_{$lang}.php (auto-generated by merge_pages.php; parts: {$foundParts})\n\n";
    $out .= "return " . _exportArray(['pages' => $mergedPages], 0) . ";\n";

    $target = $langDir . 'pages_' . $lang . '.php';
    file_put_contents($target, $out);
    echo "OK: pages_{$lang}.php (parts: {$foundParts}, stránek: " . count($mergedPages) . ")\n";
}

function _deepMerge($a, $b) {
    if (!is_array($a)) return $b;
    if (!is_array($b)) return $b;
    $isList = function ($arr) {
        if (function_exists('array_is_list')) return array_is_list($arr);
        $i = 0; foreach ($arr as $k => $_) if ($k !== $i++) return false; return true;
    };
    if ($isList($a) || $isList($b)) return $b;
    $out = $a;
    foreach ($b as $k => $v) {
        $out[$k] = isset($out[$k]) ? _deepMerge($out[$k], $v) : $v;
    }
    return $out;
}

function _exportArray($v, $indent = 0) {
    $pad = str_repeat('    ', $indent);
    if (is_array($v)) {
        if (empty($v)) return '[]';
        $isList = function_exists('array_is_list') ? array_is_list($v) : (function ($a) {
            $i = 0; foreach ($a as $k => $_) if ($k !== $i++) return false; return true;
        })($v);
        $lines = [];
        foreach ($v as $k => $vv) {
            $line = $pad . '    ';
            if (!$isList) $line .= var_export((string)$k, true) . ' => ';
            $line .= _exportArray($vv, $indent + 1);
            $lines[] = $line;
        }
        return "[\n" . implode(",\n", $lines) . ",\n" . $pad . ']';
    }
    return var_export($v, true);
}
